    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
      String courseName = (String)jComboBox1.getSelectedItem();
      String branchName = (String)jComboBox2.getSelectedItem();
      String rollno = jTextField1.getText();
      String name       = jTextField2.getText();
      String gender     = (String)jComboBox3.getSelectedItem();
      
if(rollno.equals("") || name.equals(""))
{
     JOptionPane.showMessageDialog(null,"Please Enter Some Value");
}
else
{
      try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","123456");
        Statement st = con.createStatement();
        st.executeUpdate("insert into student values('"+rollno+"','"+courseName+"','"+branchName+"','"+name+"','"+gender+"')");
        JOptionPane.showMessageDialog(null,"Data Saved Suceesfully!");
        setVisible(false);
        new adminIndex().setVisible(true);  
      }
      catch (Exception e)
      {
           JOptionPane.showMessageDialog(null,e.toString());
      }
}
    }//GEN-LAST:event_jButton6ActionPerformed

